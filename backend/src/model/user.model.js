const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    // Add more user-specific fields as needed (e.g., profile picture, status, etc.)
});

module.exports = mongoose.model("User", userSchema);
